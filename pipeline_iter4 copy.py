def get_power_constants(x,y):
  log_x = np.log(x)
  log_y = np.log(y)
  X = sm.add_constant(log_x)
  results = sm.OLS(log_y, X).fit()
  ln_A, beta = results.params
  A = np.exp(ln_A)
  results = {"A_val": A, "beta_val": beta}
  return results

